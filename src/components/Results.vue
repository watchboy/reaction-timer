<template>
  <p>Reaction time : {{score}} ms</p>
  <p class = "rank"> {{rank}}</p>
  
</template>

<script>
export default {
    props : ["score"],
    data(){
        return {
            rank: null
    
        }
    },
    mounted(){
        if(this.score < 250){
            this.rank = "고수"
        }else if (this.score < 400){
            this.rank = "킹반인"
        }else if (this.score<600){
            this.rank = "일반인"
        }else{
            this.rank = "느려"
        }
    }


}
</script>

<style>
.rank{
    font-size: 1.4em;
    color: #0faf87;
    font-weight: bold;
}

</style>